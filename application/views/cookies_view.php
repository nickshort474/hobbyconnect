<h2 id="whatarecookies">What are cookies?</h2>
<p>A cookie is a small text file that a website stores on your computer or mobile device when you visit the site.</p>
<ul>
	<li><strong>First party cookies</strong>&nbsp;are cookies set by the website you’re visiting. Only that website can read them. In addition, a website might potentially use external services, which also set their own cookies, known as <strong>third-party cookies.</strong></li>
	<li>Persistent cookies&nbsp;are cookies saved on your computer and that are not deleted automatically when you quit your browser, unlike a&nbsp;session cookie,&nbsp;which is deleted when you quit your browser.</li>
</ul>
<p>Every time you visit the Commission’s websites, you will be prompted to <strong>accept or refuse cookies.</strong></p>
<p>The purpose is to enable the site to remember your preferences (such as user name, language, etc.) for a certain period of time.</p>
<p>That way, you don’t have to re-enter them when browsing around the site during the same visit.</p>
<p>Cookies can also be used to establish anonymised statistics about the browsing experience on our sites.</p>
<p>European Commission websites mostly use “first-party cookies”. These are cookies set and controlled by the Commission, not by any external organisation.</p>
<p>However, to view some of our pages, you will have to accept cookies from external organisations. &nbsp;</p>
<p>The <strong>3 types of first-party cookie</strong> we use are to:</p>
<ul>
	<li>store visitor preferences</li>
	<li>make our websites operational</li>
	<li>gather analytics data (about user behaviour)</li>
</ul>
<p><strong>Visitor preferences</strong></p>

<h2>operational cookies</h2>

<p>There are some cookies that we have to include in order for certain web pages to function. For this reason, they do not require your consent. In particular:</p>

<ul>
	<li><strong>authentication cookies</strong></li>
	<li><strong>technical cookies</strong> required by certain IT systems</li>
</ul>
