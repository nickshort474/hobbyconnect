	<h3>What are cookies?</h3>
	<p>A cookie is a small text file that a website stores on your computer or mobile device when you visit the site.</p>
	<ul>
		<li><strong>First party cookies</strong>&nbsp;are cookies set by the website you’re visiting. Only that website can read them. In addition, a website might potentially use external services, which also set their own cookies, known as <strong>third-party cookies.</strong></li>
		<li><strong>Session cookies</strong>&nbsp;are cookies saved on your computer and that are  deleted automatically when you quit your browser, unlike a&nbsp;persistent cookie,&nbsp;which is not deleted when you quit your browser.</li>
	</ul>
	
	<p>The purpose is to enable the site to remember your preferences (such as user name, language, etc.) for a certain period of time.</p>
	<p>That way, you don’t have to re-enter them when browsing around the site during the same visit.</p>
	
	<p>PeopleFinder only uses “first-party cookies”. These are cookies set and controlled by the site, not by any external organisation.</p>
	<p>PeopleFinder only uses "Session cookies" which are deleted when you sign out or you close your browser</p>

